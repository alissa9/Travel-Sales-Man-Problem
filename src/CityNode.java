public class CityNode {

        int x;
        int y;
        int pos;

// the constructor
        public CityNode(int x, int y, int pos) {
            this.x = x;
            this.y = y;
            this.pos = pos;
        }
    //        getters and setters
        public int getX() {
            return x;
        }

        public int getY() {
            return y;
        }


    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getPos() {
        return pos;
    }

    public void setPos(int pos) {
        this.pos = pos;
    }
}
